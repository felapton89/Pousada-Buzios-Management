    private void tablaListarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaListarMousePressed
        if (evt.getClickCount() == 2) {
            int fila = tablaListar.getSelectedRow();
            String idCliente = tablaListar.getValueAt(fila, 0).toString();
            String huesped = tablaListar.getValueAt(fila, 1).toString() + " " + tablaListar.getValueAt(fila, 2).toString();
            FrmReserva.txtIdCliente.setText(idCliente);
            FrmReserva.txtHuesped.setText(huesped);
            this.dispose();
        }
    }//GEN-LAST:event_tablaListarMousePressed

