    private void tablaListartablaListarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaListartablaListarMouseClicked
        btnGuardar.setText("Editar");
        habilitarCajasDeTextoYBotones();
        accion = "editar";

        int fila = tablaListar.rowAtPoint(evt.getPoint());

        txtIdPago.setText(tablaListar.getValueAt(fila, 0).toString());
        txtNumComprobante.setText(tablaListar.getValueAt(fila, 2).toString());
        txtTotalReserva.setText(tablaListar.getValueAt(fila, 3).toString());
        dcFechaEmision.setDate(Date.valueOf(tablaListar.getValueAt(fila, 4).toString()));
        dcFechaPago.setDate(Date.valueOf(tablaListar.getValueAt(fila, 5).toString()));
        
    }//GEN-LAST:event_tablaListartablaListarMouseClicked

