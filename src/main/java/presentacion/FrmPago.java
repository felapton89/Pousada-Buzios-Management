    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Pago nuevoPago = new Pago();
        PagoService service = new PagoService();

        nuevoPago.setIdReserva(Integer.parseInt(txtIdReserva.getText()));
        nuevoPago.setNumComprobante(txtNumComprobante.getText());
        nuevoPago.setTotalPago(Double.parseDouble(txtTotalReserva.getText()));

        Calendar cal;
        int dia, mes, anio;
        cal = dcFechaEmision.getCalendar();
        dia = cal.get(Calendar.DAY_OF_MONTH);
        mes = cal.get(Calendar.MONTH);
        anio = cal.get(Calendar.YEAR) - 1900;
        nuevoPago.setFechaEmision(new Date(anio, mes, dia));
        
        cal = dcFechaPago.getCalendar();
        dia = cal.get(Calendar.DAY_OF_MONTH);
        mes = cal.get(Calendar.MONTH);
        anio = cal.get(Calendar.YEAR) - 1900;
        nuevoPago.setFechaPago(new Date(anio, mes, dia));

        if (accion.equals("guardar")) {
            if (service.insertar(nuevoPago)) {
                JOptionPane.showMessageDialog(rootPane, "El pago por $" + txtTotalReserva.getText() + " del huesped: " + txtCliente.getText() + " fue registrado con éxito.");
                llenarTabla(idReserva);
                inhabilitarCajasDeTextoYBotones();
                
                //EFECTUADO EL PAGO SE DESOCUPA LA HABITACION
                HabitacionService serviceHabitacion = new HabitacionService();
                Habitacion habitacionDesocupar = new Habitacion();
                habitacionDesocupar.setIdHabitacion(Integer.parseInt(txtIdHabitacion.getText()));
                serviceHabitacion.desocuparHabitacion(habitacionDesocupar);
                
                //EFECTUADO EL PAGO, EL ESTADO DE RESERVA PASA A SER 'PAGADO'
                ReservaService serviceReserva = new ReservaService();
                Reserva reservaPagada = new Reserva();
                reservaPagada.setIdReserva(Integer.parseInt(txtIdReserva.getText()));
                serviceReserva.reservaPagada(reservaPagada);
                
                
                
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falló el registro pago");
            }
        }

        if (accion.equals("editar")) {
            nuevoPago.setIdPago(Integer.parseInt(txtIdPago.getText()));

            if (service.editar(nuevoPago)) {
                JOptionPane.showMessageDialog(rootPane, "Pago editado con éxito");
                llenarTabla(idReserva);
                inhabilitarCajasDeTextoYBotones();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falló editar pago");
            }

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

