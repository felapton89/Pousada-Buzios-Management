    private void btnBorrarbtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarbtnBorrarActionPerformed
        if (!txtIdPago.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro que desea eliminar el pago seleccionado?", "Confirmar", 2);

            if (confirmacion == 0) {
                PagoService service = new PagoService();
                Pago pagoEliminar = new Pago();

                pagoEliminar.setIdPago(Integer.parseInt(txtIdPago.getText()));
                service.eliminar(pagoEliminar);
                llenarTabla(idReserva);
                inhabilitarCajasDeTextoYBotones();
            }
        }
    }//GEN-LAST:event_btnBorrarbtnBorrarActionPerformed


