    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtNumero.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar un número de habitación");
            txtNumero.requestFocus();
            return;
        }
        if (txtCaracteristica.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar características de la habitación");
            txtCaracteristica.requestFocus();
            return;
        }
        if (txtPrecioDiario.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar un precio diario");
            txtPrecioDiario.requestFocus();
            return;
        }

        Habitacion nuevaHabitacion = new Habitacion();
        HabitacionService service = new HabitacionService();

        nuevaHabitacion.setNumero(txtNumero.getText());
        nuevaHabitacion.setCaracteristica(txtCaracteristica.getText());
        nuevaHabitacion.setPrecioDiario(Double.parseDouble(txtPrecioDiario.getText()));
        nuevaHabitacion.setEstado((String) cboEstado.getItemAt(cboEstado.getSelectedIndex()));
        nuevaHabitacion.setTipoHabitacion((String) cboTipoHabitacion.getItemAt(cboTipoHabitacion.getSelectedIndex()));

        if (accion.equals("guardar")) {
            if (service.insertar(nuevaHabitacion)) {
                JOptionPane.showMessageDialog(rootPane, "Habitación registrada con éxito");
                llenarTabla("");
                inhabilitarCajasDeTextoYBotones();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falló el registro de habitación");
            }
        }

        if (accion.equals("editar")) {
            nuevaHabitacion.setIdHabitacion(Integer.parseInt(txtIdHabitacion.getText()));

            if (service.editar(nuevaHabitacion)) {
                JOptionPane.showMessageDialog(rootPane, "Habitación editada con éxito");
                llenarTabla("");
                inhabilitarCajasDeTextoYBotones();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falló editar habitación");
            }

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

