    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if (!txtIdHabitacion.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro que desea eliminar la habitación?", "Confirmar", 2);

            if (confirmacion == 0) {
                HabitacionService service = new HabitacionService();
                Habitacion habitacionEliminar = new Habitacion();

                habitacionEliminar.setIdHabitacion(Integer.parseInt(txtIdHabitacion.getText()));
                service.eliminar(habitacionEliminar);
                llenarTabla("");
                inhabilitarCajasDeTextoYBotones();
            }
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

