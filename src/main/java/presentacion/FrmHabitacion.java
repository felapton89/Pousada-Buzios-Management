    private void llenarTabla(String estado) {
        try {
            DefaultTableModel tableModel;
            HabitacionService service = new HabitacionService();
            tableModel = service.tableModel(estado);
            tablaListar.setModel(tableModel);
            inhabilitarCajasDeTextoYBotones();
            ocultarColumnaIdHabitacion();
            lblTotalRegistros.setText("Total de registros: " + Integer.toString(service.getTotalRegistro()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

